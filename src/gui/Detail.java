    public void setDetail(String jenis, String model, String ukuran, String pemilik, String hp_pemilik){
        this.jenis = jenis;
        modelTxt.setText(model);
        ukuranTxt.setText(ukuran);
        pemilikTxt.setText(pemilik);
        hpemilikTxt.setText(hp_pemilik);
    }
    
    public void setDetail(int harga) {
        hargaTxt.setText(Integer.toString(harga));
    }
    
    public void setDetail(byte[] gambar) {
        format = new ImageIcon(gambar);
        Image mm = format.getImage();
        int width = mobilFoto.getWidth();
        int height = mobilFoto.getHeight();
        Image img2 = mm.getScaledInstance(width, height, Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(img2);
        mobilFoto.setIcon(image);
    }
    
    public void setAllDetails(String jenis, String model, String ukuran, String pemilik, String hp_pemilik, int harga, byte[] gambar) {
        setDetail(jenis, model, ukuran, pemilik, hp_pemilik);
        setDetail(harga);
        setDetail(gambar);
        this.setVisible(true);
    }
    
    public String getModel() {
        return modelTxt.getText();
    }
    
    public String getUkuran() {
        return ukuranTxt.getText();
    }
    
    public int getHarga() {
        return Integer.parseInt(hargaTxt.getText());
    }
    
