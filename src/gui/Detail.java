    private void downloadBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_downloadBtnMouseClicked
        BufferedImage image = new BufferedImage(mobilFoto.getWidth(), mobilFoto.getHeight(), BufferedImage.TYPE_INT_ARGB);
        Graphics g = image.getGraphics();
        mobilFoto.paint(g);
        try {
            ImageIO.write(image, "png", new File("D:\\image.png"));
            System.out.println("Image saved successfully.");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_downloadBtnMouseClicked

